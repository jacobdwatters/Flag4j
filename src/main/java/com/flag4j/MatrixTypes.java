package com.flag4j;

/**
 * An enumeration of the available matrix types in Flag4j.
 */
public enum MatrixTypes {
    /**
     * Real dense matrix
     */
    MATRIX,
    /**
     * Complex dense matrix
     */
    C_MATRIX,
    /**
     * Real sparse matrix
     */
    SPARSE_MATRIX,
    /**
     * Complex sparse matrix
     */
    SPARSE_C_MATRIX,
}
