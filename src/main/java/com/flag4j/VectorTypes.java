package com.flag4j;

/**
 * An enumeration of the available matrix types in Flag4j.
 */
public enum VectorTypes {
    /**
     * Real dense vector
     */
    Vector,
    /**
     * Complex dense vector
     */
    CVector,
    /**
     * Real sparse vector
     */
    SparseVector,
    /**
     * Complex sparse vector
     */
    SparseCVector
}
